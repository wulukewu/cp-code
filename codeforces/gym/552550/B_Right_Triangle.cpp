    int x23 = x2-x3;
    int x31 = x3-x1;
    int y12 = y1-y2;
    int y23 = y2-y3;
    int y31 = y3-y1;

    bool ans = x12*x23+y12*y23==0 or x23*x31+y23*y31==0 or x31*x12+y31*y12==0;
